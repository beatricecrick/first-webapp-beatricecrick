

// index.html

const openSharkBtnElement = document.querySelector('#openShark');
const sharkElement = document.querySelector('.shark');
const sharkContentElement = sharkElement.querySelector('.shark__content')
openSharkBtnElement.addEventListener('click', () => {
    sharkElement.classList.add('open');
});
sharkContentElement.addEventListener('click', () => {
    sharkElement.classList.remove('open');
});

